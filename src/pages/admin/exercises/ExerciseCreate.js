import React, { useEffect, useState } from "react";
import { toast } from "react-toastify";
import { Button, Col, Container, Row, Form, FormLabel } from "react-bootstrap";
import styled from "styled-components";
import "../../../../node_modules/bootstrap/dist/css/bootstrap.min.css";
import PanelTitle from "../../../components/title/PanelTitle";
import { FormControlLabel, Radio, RadioGroup } from "@material-ui/core";
import { createExercise } from "../../../api/admin/exercise_api";
import AsyncSelect from "react-select/async";
import { getAxiosInstanceAuth } from "../../../api/api";

import Checkbox from "@material-ui/core/Checkbox";
import InputLabel from "@material-ui/core/InputLabel";
import ListItemIcon from "@material-ui/core/ListItemIcon";
import ListItemText from "@material-ui/core/ListItemText";
import MenuItem from "@material-ui/core/MenuItem";
import FormControl from "@material-ui/core/FormControl";
import Select from "@material-ui/core/Select";

import { MenuProps, useStyles, options } from "./utils";
import SelectEquipments from "../../../components/selects/SelectEquipments";

const ExerciseCreate = () => {
  const input = React.useRef();
  const inputFile = React.useRef();
  const inputFile2 = React.useRef();

  const [exerciseName, setExerciseName] = useState();
  const [exerciseDescription, setExerciseDescription] = useState();
  const [setNumber, setSetNumber] = useState(0);
  const [repeat, setRepeat] = useState(0);
  const [exercisetime, setExercisetime] = useState(0);
  const [level, setLevel] = useState();
  const [tip, setTip] = useState();
  const [gender, setGender] = useState();

  const [SP, setSP] = useState();

  const [imageFile, setImageFile] = useState();
  const [imagePath, setImagePath] = useState();

  const [videoURLFile, setVideoURLFile] = useState();
  const [videoURLPath, setVideoURLPath] = useState();

  const [eq, setEq] = useState([]);

  const [selectedValue, setSelectedValue] = useState(null);

  //multiselect
  const classes = useStyles();
  const [selected, setSelected] = useState([]);
  const isAllSelected =
    options.length > 0 && selected.length === options.length;

  const handleChangeMultiSelect = (event) => {
    const value = event.target.value;
    if (value[value.length - 1] === "all") {
      setSelected(selected.length === options.length ? [] : options);
      return;
    }
    setSelected(value);
    console.log(selected);
  };

  //end multiselect

  const validateData = (exercise) => {
    if (!exercise.exerciseName) return "Enter Name";
    if (!exercise.exerciseDescription) return "Enter Description";
    if (!exercise.level) return "Enter Level";
    if (!exercise.gender) return "Enter Gender";
    if (!exercise.tip) return "Enter Tip";
    if (!exercise.image) return "Enter Image";
  };

  const onChangeImg = (e) => {
    if (e.target.files && e.target.files.length > 0) {
      setImageFile(e.target.files[0]);

      const reader = new FileReader();
      reader.onload = (e) => {
        setImagePath(e.target.result);
      };
      reader.readAsDataURL(e.target.files[0]);
    }
  };

  const onChangeVideo = (e) => {
    if (e.target.files && e.target.files.length > 0) {
      setVideoURLFile(e.target.files[0]);

      const reader = new FileReader();
      reader.onload = (e) => {
        setVideoURLPath(e.target.result);
      };
      reader.readAsDataURL(e.target.files[0]);
    }
  };

  const handleCreate = () => {
    const exercise = {
      exerciseName: exerciseName,
      exerciseDescription: exerciseDescription,
      set: setNumber,
      repeat: repeat,
      exerciseTime: exercisetime,
      level: level,
      tip: tip,
      gender: gender,
      image: imageFile,
      sportsequipment: SP,
      BMIType: selected,
    };
    console.log(exercise);
    const error = validateData(exercise);
    if (error) return toast.warn(error);

    const formData = new FormData();
    formData.append("exerciseName", exerciseName);
    formData.append("exerciseDescription", exerciseDescription);
    formData.append("set", setNumber);
    formData.append("repeat", repeat);
    formData.append("exerciseTime", exercisetime);
    formData.append("level", level);
    formData.append("tip", tip);
    formData.append("gender", gender);
    formData.append("sportsequipment", SP);
    formData.append("image", imageFile);
    formData.append("videoURL", videoURLFile);
    selected.forEach((select, index) =>
      formData.append(`BMIType[${index}]`, select)
    );

    createExercise(formData, (isOK, data) => {
      if (isOK) {
        toast.success("Successful");
      } else toast.error(data);
    });
  };

  return (
    <GymWrapper>
      <Container mt={3}>
        <Row>
          <Col md={12} sm={12}>
            <PanelTitle title={"Exercise Create"} />
          </Col>
        </Row>
        <FormRow>
          <Col mt={2} md={12} sm={12}>
            <Form>
              <Form.Group className="mb-3" controlId="formBasicName">
                <FormLabel component="legend">Name</FormLabel>
                <Form.Control
                  value={exerciseName}
                  onChange={(e) => setExerciseName(e.target.value)}
                  type="text"
                  placeholder="Enter name"
                ></Form.Control>
              </Form.Group>

              <Form.Group className="mb-3" controlId="formBasicEquipment">
                <FormLabel component="legend">Equipment</FormLabel>
                <SelectEquipments
                  value={SP}
                  handleChange={(e) => setSP(e.target.value)}
                ></SelectEquipments>
              </Form.Group>

              <Form.Group className="mb-3" controlId="formBasicSet">
                <FormLabel component="legend">Number of set</FormLabel>
                <Form.Control
                  value={setNumber}
                  onChange={(e) => setSetNumber(e.target.value)}
                  type="number"
                  placeholder="Enter number of set"
                ></Form.Control>
              </Form.Group>

              <Form.Group className="mb-3" controlId="formBasicRepeat">
                <FormLabel component="legend">Repeat</FormLabel>
                <Form.Control
                  value={repeat}
                  onChange={(e) => setRepeat(e.target.value)}
                  type="number"
                  placeholder="Enter number of repeat"
                ></Form.Control>
              </Form.Group>

              <Form.Group className="mb-3" controlId="formBasicTime">
                <FormLabel component="legend">Time</FormLabel>
                <Form.Control
                  value={exercisetime}
                  onChange={(e) => setExercisetime(e.target.value)}
                  type="text"
                  placeholder="Enter time (minutes)"
                ></Form.Control>
              </Form.Group>

              <Form.Group className="mb-3" controlId="formBasicLevel">
                <FormLabel component="legend">Level</FormLabel>
                <RadioGroup
                  row
                  aria-label="level"
                  name="row-radio-buttons-group"
                  value={level}
                >
                  <FormControlLabel
                    value="easy"
                    control={<Radio />}
                    label="easy"
                    onChange={(e) => setLevel(e.target.value)}
                  />
                  <FormControlLabel
                    value="medium"
                    control={<Radio />}
                    label="medium"
                    onChange={(e) => setLevel(e.target.value)}
                  />
                  <FormControlLabel
                    value="hard"
                    control={<Radio />}
                    label="hard"
                    onChange={(e) => setLevel(e.target.value)}
                  />
                </RadioGroup>
              </Form.Group>

              <Form.Group className="mb-3" controlId="formBasicGender">
                <FormLabel component="legend">Gender</FormLabel>
                <RadioGroup
                  row
                  aria-label="gender"
                  name="row-radio-buttons-group"
                  value={gender}
                >
                  <FormControlLabel
                    value="male"
                    control={<Radio />}
                    label="male"
                    onChange={(e) => setGender(e.target.value)}
                  />
                  <FormControlLabel
                    value="female"
                    control={<Radio />}
                    label="female"
                    onChange={(e) => setGender(e.target.value)}
                  />
                  <FormControlLabel
                    value="both"
                    control={<Radio />}
                    label="both"
                    onChange={(e) => setGender(e.target.value)}
                  />
                </RadioGroup>
              </Form.Group>

              <FormControl className={classes.formControl}>
                <InputLabel id="mutiple-select-label">BMI Type</InputLabel>
                <Select
                  labelId="mutiple-select-label"
                  multiple
                  value={selected}
                  onChange={handleChangeMultiSelect}
                  renderValue={(selected) => selected.join(", ")}
                  MenuProps={MenuProps}
                >
                  <MenuItem
                    value="all"
                    classes={{
                      root: isAllSelected ? classes.selectedAll : "",
                    }}
                  >
                    <ListItemIcon>
                      <Checkbox
                        classes={{ indeterminate: classes.indeterminateColor }}
                        checked={isAllSelected}
                        indeterminate={
                          selected.length > 0 &&
                          selected.length < options.length
                        }
                      />
                    </ListItemIcon>
                    <ListItemText
                      classes={{ primary: classes.selectAllText }}
                      primary="Select All"
                    />
                  </MenuItem>
                  {options.map((option) => (
                    <MenuItem key={option} value={option}>
                      <ListItemIcon>
                        <Checkbox checked={selected.indexOf(option) > -1} />
                      </ListItemIcon>
                      <ListItemText primary={option} />
                    </MenuItem>
                  ))}
                </Select>
              </FormControl>

              <Form.Group
                className="mb-3"
                controlId="exampleForm.ControlTextarea1"
              >
                <FormLabel component="legend">Description</FormLabel>
                <Form.Control
                  value={exerciseDescription}
                  onChange={(e) => setExerciseDescription(e.target.value)}
                  as="textarea"
                  placeholder="Enter description"
                  rows={3}
                />
              </Form.Group>

              <Form.Group className="mb-3" controlId="exampleForm.ControlTip">
                <FormLabel component="legend">Tip</FormLabel>
                <Form.Control
                  value={tip}
                  onChange={(e) => setTip(e.target.value)}
                  as="textarea"
                  placeholder="Enter tip"
                  rows={3}
                />
              </Form.Group>

              {/* <Form.Group>
                <FormLabel>equipment</FormLabel>
                <AsyncSelect
                  cacheOptions
                  defaultOptions
                  value={selectedValue}
                  getOptionLabel={(e) => e.equipmentName}
                  getOptionValue={(e) => e.id}
                  loadOptions={fetchData}
                  onInputChange={handleInputChange}
                  onChange={handleChange}
                />
              </Form.Group> */}

              <Form.Group>
                <FormLabel>main image</FormLabel>
                <Form.Control
                  type={"file"}
                  ref={inputFile}
                  onChange={onChangeImg}
                ></Form.Control>
              </Form.Group>

              <Form.Group>
                <img src={imagePath} alt="" />
              </Form.Group>

              <Form.Group>
                <FormLabel>gif image</FormLabel>
                <Form.Control
                  type={"file"}
                  ref={inputFile2}
                  onChange={onChangeVideo}
                ></Form.Control>
              </Form.Group>

              <Form.Group>
                <img src={videoURLPath} alt="" />
              </Form.Group>

              <SubmitButton variant="primary" onClick={handleCreate}>
                Submit
              </SubmitButton>
            </Form>
          </Col>
        </FormRow>
      </Container>
    </GymWrapper>
  );
};

export default ExerciseCreate;

const GymWrapper = styled.div`
  margin-top: 3rem;
`;

const FormRow = styled(Row)`
  margin-top: 2rem;
`;

const SubmitButton = styled(Button)`
  margin-top: 1rem;
`;
